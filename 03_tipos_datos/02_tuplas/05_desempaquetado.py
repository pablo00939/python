# Desempaquetado de tuplas

tupla = (1, 2, 3, 4, 5)
print(tupla) # (1, 2, 3, 4, 5)
print(type(tupla)) # <class 'tuple'>
print(tupla[0]) # 1
print(tupla[1]) # 2
print(tupla[2]) # 3
print(tupla[3]) # 4
print(tupla[4]) # 5
print(len(tupla)) # 5
print(tupla[0:2]) # (1, 2)
print(tupla[2:4]) # (3, 4)
print(tupla[4:5]) # (5,)
print(tupla[0:5]) # (1, 2, 3, 4, 5)